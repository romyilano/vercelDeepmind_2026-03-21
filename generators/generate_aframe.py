#!/usr/bin/env python3
"""Generate an A-Frame HTML scene from resolved scene JSON.

Usage:
    python generators/generate_aframe.py < output/scene_resolved.json

Reads resolved scene JSON from stdin and writes an immersive A-Frame
HTML file to webXR/index.html. Copies referenced .glb assets into webXR/.
Copies scene index + scene files into webXR/scenes/ for multi-scene menu.
"""

import sys
import json
import os
import shutil

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
PROJECT_ROOT = os.path.dirname(SCRIPT_DIR)
WEBXR_DIR = os.path.join(PROJECT_ROOT, "webXR")
ASSET_MAP_PATH = os.path.join(SCRIPT_DIR, "asset_map.json")
SCENES_DIR = os.path.join(PROJECT_ROOT, "output", "scenes")

DEFAULT_PANORAMA = "punk_house.png"
DEFAULT_PANORAMA_SOURCE = os.path.join(
    PROJECT_ROOT, "assets", "images", "360", "Mexican Punk House Surf_pano.png"
)
# Minimum Y position so models float above the floor for full-body view
MIN_MODEL_Y = 0.5

